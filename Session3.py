#Single Value Container
instagram_id = "auribises"
print("Instagram id is:", instagram_id)
print("Instagram id hashcode is:", id(instagram_id))

anyName = "auribises"
print("AnyName id is:", instagram_id)
print("AnyName id hashcode is:", id(instagram_id))

#instagram_id and AnyName are Reference Variables
#They hold the hashcode of the Data

